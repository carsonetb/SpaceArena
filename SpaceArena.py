import turtle

SCREEN_WIDHT = 800
SCREEN_HEIGHT = 600

wn = turtle.Screen()
wn.setup(SCREEN_WIDHT, SCREEN_HEIGHT)
wn.title("                                                                                               Space Arena                                                                                     ")
wn.bgcolor("black")

pen = turtle.Turtle()
pen.speed(0)
pen.shape("square")
pen.color("white")
pen.penup()
pen.hideturtle()







wn.mainloop()
